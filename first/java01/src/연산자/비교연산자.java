package 연산자;

public class 비교연산자 {
//비교 연산자의 결과는 boolean(true/false : 논리형)
	public static void main(String[] args) {
		int x = 200;
		int y = 100;
		System.out.println(x == y);
		System.out.println(x != y);
		System.out.println(x > y);
		System.out.println(x < y);
		System.out.println(x <= y);
		System.out.println(x >= y);
		
		
		
	}

}
